package beans;

public class Interes_Docente {
	private int id;
	private String cod_docente;
	private	String cod_curso;
	private int cod_semestre;
	private String escuela;
	private String descripcion;
	private String int_adicional;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCod_docente() {
		return cod_docente;
	}
	public void setCod_docente(String cod_docente) {
		this.cod_docente = cod_docente;
	}
	public String getCod_curso() {
		return cod_curso;
	}
	public void setCod_curso(String cod_curso) {
		this.cod_curso = cod_curso;
	}
	public String getInt_adicional() {
		return int_adicional;
	}
	public void setInt_adicional(String int_adicional) {
		this.int_adicional = int_adicional;
	}
	public int getCod_semestre() {
		return cod_semestre;
	}
	public void setCod_semestre(int cod_semestre) {
		this.cod_semestre = cod_semestre;
	}
	public String getEscuela() {
		return escuela;
	}
	public void setEscuela(String escuela) {
		this.escuela = escuela;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	

}
