package beans;

public class Semestre {
	private int idSemestre;
	private int año;
	private int semestre;
	private int cantidadSemana;
	private int cred_especialidad;
	private int cred_general;
	
	public int getIdSemestre() {
		return idSemestre;
	}
	public void setIdSemestre(int idSemestre) {
		this.idSemestre = idSemestre;
	}
	public int getAño() {
		return año;
	}
	public void setAño(int año) {
		this.año = año;
	}
	public int getSemestre() {
		return semestre;
	}
	public void setSemestre(int semestre) {
		this.semestre = semestre;
	}
	public int getCantidadSemana() {
		return cantidadSemana;
	}
	public void setCantidadSemana(int cantidadSemana) {
		this.cantidadSemana = cantidadSemana;
	}
	public int getCred_especialidad() {
		return cred_especialidad;
	}
	public void setCred_especialidad(int cred_especialidad) {
		this.cred_especialidad = cred_especialidad;
	}
	public int getCred_general() {
		return cred_general;
	}
	public void setCred_general(int cred_general) {
		this.cred_general = cred_general;
	}
}
